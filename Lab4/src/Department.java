
public class Department {
	public int departmentID;
	public String name;
	
	public Department() {}
	
	public void setDepartmentID(int departmentID) {
		this.departmentID = departmentID;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getDepartmentID() {
		return departmentID;
	}
	
	public String getName() {
		return name;
	}
}
